# 1. Source code safety
# ---------------------------------------------------------------------------

class TestSourceCodeSafety(unittest.TestCase):
    def test_adapter_does_not_import_third_party_engines(self):
        src = inspect.getsource(_adapter)
        tree = ast.parse(src)
        forbidden = {"claude_swarm", "am_will_swarms", "clawteam", "ruflo",
                     "ecc", "paperclip", "hermes_paperclip"}
        for node in ast.walk(tree):
            if isinstance(node, (ast.Import, ast.ImportFrom)):
                for alias in getattr(node, "names", []):
                    name = alias.name or ""
                    self.assertFalse(
                        any(f in name for f in forbidden),
                        f"Adapter imports third-party engine code: {name}",
                    )

    def test_adapter_no_subprocess_launch(self):
        src = inspect.getsource(_adapter)
        tree = ast.parse(src)
        for node in ast.walk(tree):
            if isinstance(node, ast.Call):
                func = node.func
                if isinstance(func, ast.Attribute) and func.attr in ("run", "call", "Popen"):
                    if isinstance(func.value, ast.Name) and func.value.id == "subprocess":
                        self.fail("Adapter uses subprocess to launch external processes")

    def test_adapter_no_live_agent_launch_string(self):
        src = inspect.getsource(_adapter)
        forbidden_strings = ["live_launch=True", "live_agent_launch=True"]
        for s in forbidden_strings:
            self.assertNotIn(s, src, f"Adapter contains: {s}")

    def test_no_ai_attribution_in_adapter(self):
        path = _ADAPTER_DIR / "ghoti_agent_system_adapter.py"
        text = path.read_text()
        forbidden = [
            "co-authored-by claude",
            "co-authored-by gpt",
            "generated with claude",
            "generated by claude",
            "written by claude",
            "claude code",
        ]
        lower = text.lower()
        for f in forbidden:
            self.assertNotIn(f, lower, f"AI attribution found: {f!r}")


