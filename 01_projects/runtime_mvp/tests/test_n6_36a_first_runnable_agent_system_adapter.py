class TestSmokeMode(unittest.TestCase):
    def setUp(self):
        self.sandbox = _sandbox()
        if not (self.sandbox / "claude_swarm").exists():
            self.skipTest("claude_swarm not in sandbox")

    def test_smoke_accepted(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        self.assertTrue(result.get("accepted", False),
                        f"Expected accepted=True, got: {result.get('validation')}")

    def test_smoke_proves_dual_gate_participated(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        validation = result["validation"]
        self.assertTrue(validation["rust_policy_bridge_ready"])
        self.assertTrue(validation["rust_allowed"])
        self.assertTrue(validation["rust_policy_decision"]["allowed"])

    def test_smoke_selected_target_is_claude_swarm(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        self.assertEqual(result["selected_target"], "claude_swarm")

    def test_smoke_does_not_approve_external_safe_command(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        self.assertIsNone(result.get("safe_command"))
        self.assertFalse(result["candidate_external_command_approved"])

    def test_smoke_records_blocked_candidate_external_command(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        candidate = result.get("candidate_external_command", "")
        self.assertIn("--dry-run", candidate)
        self.assertIn("provider/API audit", result["candidate_external_command_warning"])

    def test_smoke_has_blocked_command(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        blocked = result.get("blocked_command", "")
        self.assertIn("claude-swarm", blocked)
        self.assertNotIn("--dry-run", blocked)

    def test_smoke_metadata_license_mit(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        self.assertEqual(result["metadata"]["license"], "MIT")

    def test_smoke_metadata_no_paths_outside_sandbox(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        metadata = result.get("metadata", {})
        for field in ["path", "safe_command", "candidate_external_command", "blocked_command", "readme_excerpt"]:
            value = metadata.get(field, "") or ""
            for p in ["/home/user/", "C:\\Users\\", "/root/"]:
                self.assertNotIn(p, value,
                                 f"Machine path in metadata.{field}: {p}")
        self.assertEqual(
            metadata["path"],
            "21_repos/third_party_runtime_sandbox/claude_swarm",
        )

    def test_check_uses_repo_relative_sandbox_path(self):
        result = _adapter._run_check(sandbox=self.sandbox)
        self.assertEqual(result["sandbox_root"], "21_repos/third_party_runtime_sandbox")

    def test_smoke_safety_block_correct(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        sb = result["safety_block"]
        self.assertTrue(sb["simulation"])
        self.assertFalse(sb["live_execution"])
        self.assertFalse(sb["live_agent_launch"])
        self.assertFalse(sb["third_party_code_executed"])
        self.assertFalse(sb["third_party_code_imported"])

    def test_smoke_has_deferred_systems(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        self.assertIn("deferred_systems", result)
        deferred = result["deferred_systems"]
        for system in ["am_will_swarms", "ruflo", "ecc", "paperclip"]:
            self.assertIn(system, deferred)

    def test_smoke_metadata_third_party_not_imported(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        self.assertFalse(result["metadata"]["third_party_code_imported"])
        self.assertFalse(result["metadata"]["third_party_code_executed"])

    def test_external_dry_run_truth_is_recorded(self):
        result = _adapter._run_smoke(sandbox=self.sandbox)
        metadata = result["metadata"]
        self.assertTrue(metadata["external_dry_run_requires_api_key"])
        self.assertTrue(metadata["external_dry_run_calls_provider"])
        self.assertTrue(metadata["external_dry_run_writes_session"])


