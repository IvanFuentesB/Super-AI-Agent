class TestSchemaValidation(unittest.TestCase):
    def _bad_fixture(self, **overrides) -> dict:
        f = _load_fixture()
        def deep_set(d, keys, value):
            for k in keys[:-1]:
                d = d[k]
            d[keys[-1]] = value
        for key, value in overrides.items():
            if "." in key:
                parts = key.split(".")
                deep_set(f, parts, value)
            else:
                f[key] = value
        return f

    def test_wrong_source_rejected(self):
        data = self._bad_fixture(source="live_run")
        errors = _replay._validate_fixture_schema(data)
        self.assertTrue(any("source" in e for e in errors))

    def test_live_execution_true_rejected(self):
        f = _load_fixture()
        f["swarm"]["live_execution"] = True
        errors = _replay._validate_fixture_schema(f)
        self.assertTrue(any("live_execution" in e for e in errors))

    def test_simulation_false_rejected(self):
        f = _load_fixture()
        f["swarm"]["simulation"] = False
        errors = _replay._validate_fixture_schema(f)
        self.assertTrue(any("simulation" in e for e in errors))

    def test_safety_live_agent_launch_true_rejected(self):
        f = _load_fixture()
        f["safety"]["live_agent_launch"] = True
        errors = _replay._validate_fixture_schema(f)
        self.assertTrue(any("live_agent_launch" in e for e in errors))

    def test_duplicate_task_id_rejected(self):
        f = _load_fixture()
        f["tasks"].append({**f["tasks"][0]})
        errors = _replay._validate_fixture_schema(f)
        self.assertTrue(any("duplicate" in e for e in errors))


