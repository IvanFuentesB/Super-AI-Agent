class TestSourceSafety(unittest.TestCase):
    def test_no_ai_attribution(self):
        path = _WRAPPER_DIR / "ghoti_claude_swarm_fixture_replay.py"
        text = path.read_text().lower()
        for phrase in ["co-authored-by claude", "generated with claude",
                       "generated by claude", "written by claude"]:
            self.assertNotIn(phrase, text, f"AI attribution: {phrase!r}")

    def test_no_api_key_hardcoded(self):
        path = _WRAPPER_DIR / "ghoti_claude_swarm_fixture_replay.py"
        text = path.read_text()
        self.assertNotIn("sk-ant-", text)

    def test_wrapper_does_not_import_claude_swarm(self):
        src = inspect.getsource(_replay)
        tree = ast.parse(src)
        for node in ast.walk(tree):
            if isinstance(node, (ast.Import, ast.ImportFrom)):
                for alias in getattr(node, "names", []):
                    self.assertNotIn("claude_swarm", alias.name or "")

    def test_no_subprocess_launch(self):
        src = inspect.getsource(_replay)
        tree = ast.parse(src)
        for node in ast.walk(tree):
            if isinstance(node, ast.Call):
                func = node.func
                if isinstance(func, ast.Attribute) and func.attr in ("run", "call", "Popen"):
                    if isinstance(func.value, ast.Name) and func.value.id == "subprocess":
                        self.fail("Wrapper uses subprocess to launch processes")

    def test_no_third_party_code_committed(self):
        result = subprocess.run(
            ["git", "-C", str(_repo_root()), "ls-files",
             "21_repos/third_party_runtime_sandbox/claude_swarm"],
            capture_output=True, text=True,
        )
        self.assertEqual(result.stdout.strip(), "")


